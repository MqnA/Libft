#include "libft.h"

void	*ft_memcpy(void *dst, const void *src, size_t n)
{
	char	*s1;
	char	*s2;

	s1 = (char *)dst;
	s2 = (char *)src;
	if (n == 0 || dst == src)
		return (dst);
	while (--n)
		*s1++ = *s2++;
	*s1 = *s2;
	return (dst);
}
