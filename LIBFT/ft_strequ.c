#include "libft.h"

int		ft_strequ(const char *s1, char const *s2)
{
	if (!s1 && !s2)
		return (1);
	if (!s1 || !s2)
		return (0);
	while (*s1++)
	{
		s2++;
		if (*s1 != *s2)
			return (0);
	}
	return (1);
}
